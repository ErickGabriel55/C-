using System;

/* Essa aula fala criação de constantes */
namespace Aula_3.aula3{
    class Program
    {

        static void Main(String[] args)
        {
            const float PI = 3.1415926535f; // Para criar uma constante é necessário a palavra reservada const

            // Uma constante é utilizada quando um valor precia não pode ser modificado para que o seu programa funcione
            Console.WriteLine(PI + " Erick");
        }

    }
}
